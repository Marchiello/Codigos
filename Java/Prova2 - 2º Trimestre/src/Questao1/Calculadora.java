package Questao1;

public class Calculadora{

    public static double somar(double numero1, double numero2){
        double soma = numero1 + numero2;
        return soma;

    }

    public static double subtrair(double numero1, double numero2){
        double subtração = numero1 - numero2;
        return subtração;
    }

    public static double multiplicar(double numero1, double numero2){
        double multiplicação = numero1 * numero2;
        return multiplicação;
    }

    public static double divisão(double numero1, double numero2){
        double divisão = numero1 / numero2;
        return divisão;
    }

    public static double mod(double numero1, double numero2){
        double mod = numero1 % numero2;
        return mod;
    }

}